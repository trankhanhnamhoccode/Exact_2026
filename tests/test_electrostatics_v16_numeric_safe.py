def test_triangle_distances_field_query_stays_field_not_force():
    problem = (
        "Two electric charges q1 = +3 × 10^-6 C and q2 = +4 × 10^-6 C are placed at points A and B, which are 5 cm apart. "
        "Calculate the resultant electric field strength at point M, which is 3 cm from A and 4 cm from B."
    )
    assert any(str(ans).endswith("V/m") and _num(ans) == pytest.approx(3.75e7, rel=2e-3) for ans in _answers(problem) if ans)


def test_parallel_capacitor_voltage_under_limit_candidate():
    problem = (
        "Two capacitors with capacitances C1 = 0.4μF and C2 = 0.6μF are connected in parallel, and then connected to a power source with a voltage U < 60 V. "
        "One of the two capacitors has a charge of 3 × 10^-5 C. Calculate the voltage U."
    )
    out = solve_problem_with_llm(problem, FakeSchemaLLM("{}"), k=2)
    assert out.solve_result.status == "ok"
    assert _num(out.solve_result.answer) == pytest.approx(50.0, rel=1e-9)


