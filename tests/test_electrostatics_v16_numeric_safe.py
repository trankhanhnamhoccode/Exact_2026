class FakeSchemaLLM:
    def __init__(self, output: str = "{}"):
        self.output = output

    def generate(self, prompt: str) -> str:
        return self.output


def _num(answer: object) -> float:
    m = re.search(r"[-+]?\d+(?:\.\d+)?(?:[eE][-+]?\d+)?", str(answer))
    assert m, f"No number found in answer: {answer!r}"
    return float(m.group(0))


def _answers(problem: str) -> list[object]:
    return [solve_schema(schema).answer for schema in generate_equations_candidate_schemas(problem)]


def test_midpoint_between_opposite_charges_keeps_both_sources():
    problem = (
        "In a vacuum, consider two electric charges q1 = -q2 = 10^-7 C placed at points A and B, "
        "separated by 8 cm. Determine the resultant force acting on electric charge q0 = 10^-7 C "
        "when q0 is placed at H, which is the midpoint of AB."
    )
    assert any(_num(ans) == pytest.approx(1.125e-1, rel=2e-3) for ans in _answers(problem) if ans)


def test_symmetric_midpoint_and_center_zero_rules():
    midpoint = (
        "Two point charges, q1 and q2, of equal magnitude and the same sign, are placed in air a distance r apart. "
        "A third point charge, q3, is placed at the midpoint of the line segment connecting q1 and q2. "
        "What is the net force acting on charge q3?"
    )
    square = (
        "Four identical charges q1, q2, q3, q4 are placed at the four vertices of a square ABCD, "
        "with a side length of 4 cm, respectively. Find the electric force acting on a charge q0 = 2×10^-8 C placed at the center of the square."
    )
    equilateral = (
        "Three identical charges 'q' are placed at the three vertices of an equilateral triangle with side length 'a'. "
        "Find the net electric force acting on a test charge placed at the center of the triangle?"
    )
    alternating_square = (
        "Four charges of the same magnitude q are placed at the four vertices of a square ABCD with side length a. "
        "Positive charges are located at A and C, while negative charges are located at B and D. "
        "Determine the resultant electric field strength at the intersection point of the square's diagonals."
    )
    for problem in [midpoint, square, equilateral, alternating_square]:
        assert any(_num(ans) == pytest.approx(0.0, abs=1e-12) for ans in _answers(problem) if ans)


